class Animal {
  final String nome;
  final String categoria;
  final String descrizione;
  final String immagine;

  final String peso;
  final String vitaMedia;
  final String alimentazione;
  final String curiosita;
  final String problemiComuni;


  Animal({
    required this.nome,
    required this.categoria,
    required this.descrizione,
    required this.immagine,

    required this.peso,
    required this.vitaMedia,
    required this.alimentazione,
    required this.curiosita,
    required this.problemiComuni,
  });
}